// Operators

// Aritmethic

// Dart supports this aritmethics operators
// + -> Add
// - -> Sustract
// * -> Multiply
// / -> Division
// % -> Module (It returns the remainder of a division)

// Dart also supports increment or decrement operators like these:
// ++var -> It reads like var = var + 1
// var++ -> Like the previous one
// --var -> This is the opposite of the first one. It reads like var = var - 1

// Relations and equality

// == Equal
// != Different
// > Greater than
// < Smaller than
// >= Greater than or equal
// <= Smaller than or equal

// Verification

// This operators are useless beacause you can verify data types on runtime.
